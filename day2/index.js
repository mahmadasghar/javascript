// 1: Difference between let & var variable assignment
//      with let, you can't initialize another variable with the same name in a let expression
//      while you can do this with var.
// let someVariable = 78
// var anotherVariable = 57

// 2: Rules for naming variables
//      i: name cannot start with a number
//          let 4des = 134
//      ii: name cannot contain any special characters except for an underscore (_) (note: no space allowed as well)
        //  let variable_dsfsd = 45
        //  let dfsf&^sdj = 45
//      iii: cannot use language reserved keywords (i.e. let, const, var, class function, for, while, do, new, true, false etc)
        //  let let = 234
        //  let const = 32
        //  let class = 345
        //  let function = 34
        //  let new = 98

// 3: Conventions for naming variables
//      i: Camel Case:
            let someVariableIsThis = 123
//      ii: Pascal Case:
            let SomeVariableIsThis = 123
//      iii: snake case:
            let some_variable_is_this = 123
//      iv: Capital Snake Case: (used to name constants with hard coded values)
            let SOME_VARIABLE_IS_THIS = 321 

